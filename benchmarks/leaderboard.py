"""Public reliability leaderboard (newplan M24) — score agents across the scenario suite.

Records each example agent once, runs the full adversarial reliability suite against it
(deterministic, seeded — no live models), and ranks them by a single **Volo Score** (0-100, the
mean of the four DFAH dimensions after the p5 aggregation). Emits machine-readable JSON plus a
self-contained static HTML page + a Markdown table — regenerated by a GitHub Actions cron
(`examples/workflows/volo-leaderboard.yml`). $0 infra: the artifact is static.

Run:  uv run python benchmarks/leaderboard.py
"""

from __future__ import annotations

import json
import sys
from pathlib import Path
from typing import Any

_ROOT = Path(__file__).resolve().parent.parent
if str(_ROOT) not in sys.path:
    sys.path.insert(0, str(_ROOT))

from volo_core import Recording, current_recorder  # noqa: E402
from volo_reliability import METRIC_NAMES, ReliabilityReport, faithfulness  # noqa: E402
from volo_runner import OrchestratorConfig, orchestrate, resolve_agent  # noqa: E402
from volo_sdk import Recorder, RecorderConfig  # noqa: E402

# The scored entries. Each example agent is recorded, then run through the scenario suite.
ENTRIES: list[dict[str, Any]] = [
    {
        "name": "echo_agent",
        "target": "examples.echo_agent:run",
        "input": {"text": "hello"},
        "framework": "raw",
    },
    {
        "name": "calc_agent",
        "target": "examples.calc_agent:run",
        "input": {"a": 2, "b": 3, "c": 4},
        "framework": "raw",
    },
    {
        "name": "calc_agent_v2 (off-by-one)",
        "target": "examples.calc_agent_v2:run",
        "input": {"a": 2, "b": 3, "c": 4},
        "framework": "raw",
    },
    {
        "name": "research_agent",
        "target": "examples.research_agent:run",
        "input": {"query": "volo"},
        "framework": "raw",
    },
    {
        "name": "flaky_agent (nondeterministic)",
        "target": "examples.flaky_agent:run",
        "input": {},
        "framework": "raw",
    },
]

N_RUNS = 3
SEED = 0


def _record(target: str, agent_input: dict[str, Any]) -> Recording:
    fn = resolve_agent(target)
    rec = Recorder(config=RecorderConfig(apply_redaction=False))
    with current_recorder(rec):
        rec.set_final_output(fn(agent_input))
    return rec.recording


def _volo_score(baseline_faithfulness: float, report: ReliabilityReport) -> int:
    """0-100: half clean-run correctness (is the answer grounded normally?), half adversarial
    robustness (the four dimensions averaged under the scenario suite). Rewards agents that are
    both *correct* and *robust* — a wrong-but-stable agent scores low on the first half."""
    dims = [report.aggregate.get(m, 0.0) for m in METRIC_NAMES]
    robustness = sum(dims) / len(dims)
    return round(100 * (0.5 * baseline_faithfulness + 0.5 * robustness))


def _dominant_model(recording: Recording) -> str:
    counts: dict[str, int] = {}
    for step in recording.steps:
        if step.payload.type == "model_call":
            key = f"{step.payload.provider}/{step.payload.model}"
            counts[key] = counts.get(key, 0) + 1
    return max(counts, key=lambda k: counts[k]) if counts else "-"


def score_entry(entry: dict[str, Any]) -> dict[str, Any]:
    baseline = _record(entry["target"], entry["input"])
    baseline_faithfulness = faithfulness(baseline)
    report = orchestrate(
        baseline,
        resolve_agent(entry["target"]),
        config=OrchestratorConfig(n_runs=N_RUNS, seed=SEED, agent_input=entry["input"]),
    )
    by_class: dict[str, list[float]] = {}
    for s in report.scenarios:
        vals = [v for v in s.metrics.values() if isinstance(v, int | float)]
        if vals:
            by_class.setdefault(s.failure_class, []).extend(vals)
    return {
        "name": entry["name"],
        "framework": entry["framework"],
        "model": _dominant_model(baseline),
        "volo_score": _volo_score(baseline_faithfulness, report),
        "verdict": report.verdict,
        "baseline_faithfulness": round(baseline_faithfulness, 3),
        "dimensions": {m: round(report.aggregate.get(m, 0.0), 3) for m in METRIC_NAMES},
        "by_failure_class": {k: round(sum(v) / len(v), 3) for k, v in sorted(by_class.items())},
        "n_scenarios": len(report.scenarios),
        "n_steps": len(baseline.steps),
    }


def build_leaderboard() -> list[dict[str, Any]]:
    rows = [score_entry(e) for e in ENTRIES]
    rows.sort(key=lambda r: (-r["volo_score"], r["name"]))
    for i, row in enumerate(rows, start=1):
        row["rank"] = i
    return rows


def render_markdown(rows: list[dict[str, Any]]) -> str:
    lines = [
        "# Volo Reliability Leaderboard",
        "",
        "Example agents scored across the full adversarial scenario suite "
        f"(deterministic, seed={SEED}, {N_RUNS} runs/scenario). "
        "**Volo Score** = 50% clean-run correctness + 50% adversarial robustness (0-100).",
        "",
        "| Rank | Agent | Framework | Volo Score | Verdict | Baseline | Trajectory | Decision | Faithful | Consistency |",
        "|---|---|---|---|---|---|---|---|---|---|",
    ]
    for r in rows:
        d = r["dimensions"]
        lines.append(
            f"| {r['rank']} | {r['name']} | {r['framework']} | **{r['volo_score']}** | "
            f"{r['verdict']} | {r['baseline_faithfulness']:.2f} | "
            f"{d['trajectory_determinism']:.2f} | {d['decision_determinism']:.2f} | "
            f"{d['faithfulness']:.2f} | {d['consistency_under_repetition']:.2f} |"
        )
    lines.append("")
    return "\n".join(lines)


def render_html(rows: list[dict[str, Any]]) -> str:
    def cell(v: float) -> str:
        color = "#12b886" if v >= 0.9 else "#f59f00" if v >= 0.6 else "#fa5252"
        return f'<td style="text-align:right;color:{color};font-variant-numeric:tabular-nums">{v:.2f}</td>'

    body = []
    for r in rows:
        d = r["dimensions"]
        verdict_color = "#12b886" if r["verdict"] == "ship" else "#fa5252"
        body.append(
            f"<tr><td>{r['rank']}</td><td><b>{r['name']}</b></td><td>{r['framework']}</td>"
            f'<td style="text-align:right"><b>{r["volo_score"]}</b></td>'
            f'<td style="color:{verdict_color}">{r["verdict"]}</td>'
            f"{cell(r['baseline_faithfulness'])}"
            f"{cell(d['trajectory_determinism'])}{cell(d['decision_determinism'])}"
            f"{cell(d['faithfulness'])}{cell(d['consistency_under_repetition'])}</tr>"
        )
    return _HTML_TEMPLATE.format(seed=SEED, n_runs=N_RUNS, rows="\n".join(body))


_HTML_TEMPLATE = """<!doctype html>
<html lang="en"><head><meta charset="utf-8">
<meta name="viewport" content="width=device-width,initial-scale=1">
<title>Volo Reliability Leaderboard</title>
<style>
  body{{background:#0A0E14;color:#c9d1d9;font:15px/1.5 ui-monospace,SFMono-Regular,Menlo,monospace;
    max-width:900px;margin:3rem auto;padding:0 1rem}}
  h1{{color:#e6edf3;font-weight:600;letter-spacing:-.02em}}
  .sub{{color:#768390;margin-bottom:2rem}}
  table{{width:100%;border-collapse:collapse}}
  th,td{{padding:.5rem .6rem;border-bottom:1px solid #21262d;text-align:left}}
  th{{color:#768390;font-weight:400;text-transform:uppercase;font-size:11px;letter-spacing:.08em}}
  tr:hover td{{background:#0d1117}}
</style></head><body>
<h1>Volo Reliability Leaderboard</h1>
<p class="sub">Example agents scored across the full adversarial scenario suite —
deterministic (seed={seed}, {n_runs} runs/scenario). Volo Score = 50% clean-run correctness +
50% adversarial robustness (0-100).</p>
<table><thead><tr>
<th>#</th><th>Agent</th><th>Framework</th><th style="text-align:right">Volo Score</th><th>Verdict</th>
<th style="text-align:right">Baseline</th>
<th style="text-align:right">Trajectory</th><th style="text-align:right">Decision</th>
<th style="text-align:right">Faithful</th><th style="text-align:right">Consistency</th>
</tr></thead><tbody>
{rows}
</tbody></table>
</body></html>
"""


def main() -> None:
    rows = build_leaderboard()
    out_dir = _ROOT / "benchmarks"
    (out_dir / "leaderboard.json").write_text(
        json.dumps({"seed": SEED, "n_runs": N_RUNS, "rows": rows}, indent=2) + "\n",
        encoding="utf-8",
    )
    (out_dir / "leaderboard.md").write_text(render_markdown(rows), encoding="utf-8")
    (out_dir / "leaderboard.html").write_text(render_html(rows), encoding="utf-8")

    print(render_markdown(rows))
    print(f"\nwrote leaderboard.json / .md / .html -> {out_dir}")


if __name__ == "__main__":
    main()
